$(document).ready(setMenu);
var flagMenu=1;
function setMenu(){
    $('.menu-btn').click(function(){
        if(flagMenu == 1){
            $('nav').animate({left:'0'});
            flagMenu=0;
        }else{
            $('nav').animate({left:'-280px'});
            flagMenu=1;
        }
    });
}

function refreshMenu(){
	if(flagMenu == 1){
        $('nav').animate({left:'0'});
        flagMenu=0;
    }else{
        $('nav').animate({left:'-280px'});
        flagMenu=1;
    }
}